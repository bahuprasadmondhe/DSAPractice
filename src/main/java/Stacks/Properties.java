package Stacks;

public
class Properties {

  String name;
  String label;
  Integer SN;
  Integer severity;
  String message;
  String source;

  public
  String getName() {
    return name;
  }

  public
  void setName(String name) {
    this.name = name;
  }

  public
  String getLabel() {
    return label;
  }

  public
  void setLabel(String label) {
    this.label = label;
  }

  public
  Integer getSN() {
    return SN;
  }

  public
  void setSN(Integer SN) {
    this.SN = SN;
  }

  public
  Integer getSeverity() {
    return severity;
  }

  public
  void setSeverity(Integer severity) {
    this.severity = severity;
  }

  public
  String getMessage() {
    return message;
  }

  public
  void setMessage(String message) {
    this.message = message;
  }

  public
  String getSource() {
    return source;
  }

  public
  void setSource(String source) {
    this.source = source;
  }
}
